package aula13;

public class Result {

	private int swaps;
	private int[] array;

	public Result(int swaps, int[] array) {
		this.swaps = swaps;
		this.array = array;
	}

	public int getSwaps() {
		return swaps;
	}

	public void setSwaps(int swaps) {
		this.swaps = swaps;
	}

	public int[] getArray() {
		return array;
	}

	public void setArray(int[] array) {
		this.array = array;
	}

}
